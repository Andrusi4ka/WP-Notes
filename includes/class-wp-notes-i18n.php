	public function current_language() {
		$locale = function_exists('determine_locale') ? determine_locale() : get_locale();
		$locale = strtolower((string) $locale);

		if (0 === strpos($locale, 'no') || 0 === strpos($locale, 'nb') || 0 === strpos($locale, 'nn')) {
			return 'no';
		}

		if (0 === strpos($locale, 'en')) {
			return 'en';
		}

		return 'en';
	}

	public function t($key) {
		$language = $this->current_language();
		if (isset($this->strings[$language][$key])) {
			return $this->strings[$language][$key];
		}

		return isset($this->strings['en'][$key]) ? $this->strings['en'][$key] : $key;
	}
